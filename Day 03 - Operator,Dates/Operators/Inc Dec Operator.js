//Increament Operator
//pre
let preInc = 0
console.log(++preInc) // 1
console.log(preInc) // 1

//post
let postInc = 0
console.log(postInc++) // 0
console.log(postInc) // 1


//Decrement Operator
//pre
let preDec = 0
console.log(--preDec) // -1
console.log(preDec) // -1

//post
let postDec = 0
console.log(postDec--) //0
console.log(postDec--) //-1